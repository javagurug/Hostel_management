                pt.setString(11, adhar);
                pt.setString(12, block);
                pt.setString(13, room);
                pt.setString(14, status);
                JOptionPane.showMessageDialog(null, "SUCCESSFULY ");
                pt.setDate(15, null);
                pt.executeUpdate();

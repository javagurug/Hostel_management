                pt.setDate(2, (java.sql.Date) joi);
                pt.setString(3, name);
                pt.setString(4, fathername);
                pt.setString(5, mothername);
                pt.setDate(6, (java.sql.Date) dob1);
                pt.setString(7, email);
                pt.setString(8, mobilenumber);
                pt.setString(9, department);
